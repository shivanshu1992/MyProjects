package Java_Basic;

public class PostIncrmntDecrmnt {

	public static void main(String[] args) {
		
		//post decrement
		
		int i=1;
		int j=i++; // In this 1st, i will be given to j then i is incremented by 1
	    System.out.println("value of i is " +  i);
	    System.out.println("value of j is " +   j);
	    
	    //pre increment
	    
	    int k=1;
		int m=++k; // In this 1st, k will be incremenetd by 1 then value will be given to m
	    System.out.println("value of k is " +  k);
	    System.out.println("value of m is " +  m );
	    
	    
	    
	    
	    
	}

}
